#!/usr/bin/env node

import fs from 'node:fs';
import path from 'node:path';

const repoRoot = path.resolve(new URL('..', import.meta.url).pathname);
const outputPath = path.join(repoRoot, 'docs', 'agent-code-map.md');

function read(file) {
  return fs.existsSync(file) ? fs.readFileSync(file, 'utf8') : '';
}

function walk(dir, predicate = () => true) {
  if (!fs.existsSync(dir)) return [];
  const out = [];
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    if (entry.name === 'node_modules' || entry.name === '.git' || entry.name === '.worktrees') continue;
    const full = path.join(dir, entry.name);
    if (entry.isDirectory()) out.push(...walk(full, predicate));
    if (entry.isFile() && predicate(full)) out.push(full);
  }
  return out;
}

function rel(file) {
  return path.relative(repoRoot, file);
}

function frontMatterValue(text, key) {
  const match = text.match(new RegExp(`^${key}:\\s*(.+)$`, 'm'));
  return match ? match[1].trim() : '';
}

function listBlock(text, key) {
  const match = text.match(new RegExp(`^${key}:\\n((?:\\s+-\\s+.+\\n?)+)`, 'm'));
  if (!match) return '';
  return match[1]
    .split('\n')
    .map((line) => line.trim().replace(/^- /, ''))
    .filter(Boolean)
    .join(', ');
}

function siblingTests(dir) {
  return walk(dir, (file) => /\.(test|spec)\.(ts|tsx)$/.test(file)).map(rel).sort();
}

function topFiles(dir) {
  return walk(dir, (file) => /\.(ts|tsx)$/.test(file) && !/\.(test|spec)\.(ts|tsx)$/.test(file))
    .map((file) => ({ file, lines: read(file).split('\n').length }))
    .sort((a, b) => b.lines - a.lines)
    .slice(0, 5)
    .map(({ file, lines }) => `${rel(file)} (${lines} lines)`);
}

const packageJson = JSON.parse(read(path.join(repoRoot, 'package.json')));
const featureRoot = path.join(repoRoot, 'src', 'features');
const featureDirs = fs.readdirSync(featureRoot, { withFileTypes: true })
  .filter((entry) => entry.isDirectory())
  .map((entry) => entry.name)
  .sort();

const lines = [];
lines.push('# Agent Code Map');
lines.push('');
lines.push('Generated by `npm run agent:map`. Keep this file committed so coding agents can orient quickly without rediscovering the repo shape every turn.');
lines.push('');
lines.push('## Verification Commands');
lines.push('');
for (const script of ['verify:changed', 'code:health', 'test:code-health', 'lint', 'lint:tests', 'test', 'test:ci', 'lint:supabase-functions', 'product:lint', 'architecture:lint', 'mcp:smoke', 'mcp:e2e']) {
  if (packageJson.scripts?.[script]) {
    lines.push(`- \`npm run ${script}\` - \`${packageJson.scripts[script]}\``);
  }
}
lines.push('');
lines.push('## Completion Ritual');
lines.push('');
lines.push('- Before handing work back, run `npm run verify:changed -- --run` so the current diff selects and executes the relevant local gates.');
lines.push('- `npm run code:health -- --fail-on-regression` protects the repo from new large-file, loose-typing, raw-Text, and direct-Reusables drift while leaving legacy debt warning-only.');
lines.push('- Add `npm test -- --runInBand` when shared config, shared stores/services, or test infrastructure changed.');
lines.push('- CI enforces the stable repo-wide gates: code health, app typecheck, test typecheck, Supabase function typecheck, product lint, architecture lint, and coverage Jest.');
lines.push('');
lines.push('## Primary Code Areas');
lines.push('');
lines.push('- `src/features/` - user-facing feature surfaces, each with a `FEATURE.md` manifest except dev-only tooling.');
lines.push('- `src/domain/` - pure domain types, workflow specs, limits, and transformation logic.');
lines.push('- `src/services/` - backend, AI, notification, analytics, sync, calendar, and integration clients.');
lines.push('- `src/store/` - Zustand stores and app state lifecycle logic.');
lines.push('- `src/ui/` - shared app UI adapters and layout primitives. Feature code should use this layer instead of raw Reusables.');
lines.push('- `supabase/functions/` - Deno Edge Functions, checked by `npm run lint:supabase-functions`.');
lines.push('- `packages/arc-survey`, `packages/kwilt-sdk`, `packages/kwilt-tokens` - workspace packages shared with app and connector surfaces.');
lines.push('');
lines.push('## Feature Manifests');
lines.push('');
lines.push('| Feature | Persona | Hero JTBD | Serves | Key files | Tests |');
lines.push('|---|---|---|---|---|---|');

for (const feature of featureDirs) {
  const dir = path.join(featureRoot, feature);
  const manifestPath = path.join(dir, 'FEATURE.md');
  const manifest = read(manifestPath);
  const personas = frontMatterValue(manifest, 'personas') || '';
  const hero = frontMatterValue(manifest, 'hero_jtbd') || '';
  const serves = listBlock(manifest, 'serves') || '';
  const keyFiles = topFiles(dir).join('<br>');
  const tests = siblingTests(dir).slice(0, 6).join('<br>');
  lines.push(`| \`${feature}\` | ${personas} | ${hero} | ${serves} | ${keyFiles || '-'} | ${tests || '-'} |`);
}

lines.push('');
lines.push('## Agent Routing Heuristics');
lines.push('');
lines.push('- Touching `supabase/functions/**` usually requires `npm run lint:supabase-functions`; tests under `_shared/__tests__` are Deno-oriented and outside the npm/Jest app gate.');
lines.push('- Touching `docs/jtbd`, `docs/personas`, `docs/job-flows`, `docs/feature-briefs`, or `src/features/*/FEATURE.md` requires `npm run product:lint`.');
lines.push('- Touching feature UI should run `npm run architecture:lint`; heed warnings when already editing files with legacy raw `Text` imports.');
lines.push('- Touching large screens should prefer extracting pure helpers/hooks with focused tests over broad inline edits.');
lines.push('- New code should stay inside the `code:health` ratchets; if a large-file or loose-typing exception is intentional, make the exception explicit in review instead of quietly expanding the baseline.');
lines.push('- Use `npm run verify:changed` at the start and end of an agent session to choose the right checks for the current diff.');
lines.push('');

fs.writeFileSync(outputPath, `${lines.join('\n')}\n`);
console.log(`Wrote ${rel(outputPath)}`);
